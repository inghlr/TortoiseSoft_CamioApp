/// Application-wide dimension constants for consistent spacing, sizing, and layout.
/// 
/// This class defines all the dimensional values used throughout the application,
/// including padding sizes, border radius values, and icon sizes.
/// Using these constants ensures consistency across the entire UI and makes
/// responsive design changes easier to implement globally.
class AppDimensions {
  // Padding/Spacing values
  /// Extra small padding (4 dp)
  /// Used for minimal spacing between elements
  static const double paddingXSmall = 4.0;

  /// Small padding (8 dp)
  /// Used for tight spacing between related elements
  static const double paddingSmall = 8.0;

  /// Medium padding (16 dp)
  /// Standard spacing for most components
  static const double paddingMedium = 16.0;

  /// Large padding (24 dp)
  /// Used for major section separations
  static const double paddingLarge = 24.0;

  /// Extra large padding (32 dp)
  /// Used for maximum spacing between distinct sections
  static const double paddingXLarge = 32.0;

  // Border radius values
  /// Small border radius (4 dp)
  /// Used for subtle rounded corners
  static const double borderRadiusSmall = 4.0;

  /// Medium border radius (8 dp)
  /// Standard rounded corners for cards and buttons
  static const double borderRadiusMedium = 8.0;

  /// Large border radius (12 dp)
  /// Used for more pronounced rounded corners
  static const double borderRadiusLarge = 12.0;

  // Icon sizes
  /// Small icon size (20 dp)
  /// Used for inline icons and small decorative elements
  static const double iconSizeSmall = 20.0;

  /// Medium icon size (24 dp)
  /// Standard size for most icon uses
  static const double iconSizeMedium = 24.0;

  /// Large icon size (48 dp)
  /// Used for prominent icons and decorative elements
  static const double iconSizeLarge = 48.0;

  /// Extra large icon size (80 dp)
  /// Used for main illustrations or large decorative icons
  static const double iconSizeXLarge = 80.0;

  // Floating Action Button sizes
  /// Standard FAB size (56 dp)
  /// Default size for floating action buttons
  static const double fabSize = 56.0;

  /// Mini FAB size (40 dp)
  /// Used for smaller floating action buttons
  static const double fabSizeMini = 40.0;
}
